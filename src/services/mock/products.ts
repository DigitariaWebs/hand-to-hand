function makeImages(category: string, count = 3): Product['images'] {
  const imgs = imagesByCategory[category] ?? imagesByCategory['electronique'];
  return Array.from({ length: Math.min(count, imgs.length) }, (_, i) => ({
    id: `img-${category}-${i}`,
    url: imgs[i],
    thumbnail: imgs[i].replace('w=400', 'w=200'),
  }));
}

